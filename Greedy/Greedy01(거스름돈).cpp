#include <bits/stdc++.h>

using namespace std;

int n=1260;
int arr[4]={500,100,50,10};

int main(){
    int count=0;
    
    for(int i=0; i<4; i++){
        count+=n/arr[i];
        n=n%arr[i];
    }
    
    cout<<count<<" ";
    
    return 0;
}

